"""
เขียนโปรแกรมรับจำนวนเต็มบวก 1 จำนวน n

จากนั้นสร้าง pattern แบบนี้:

ถ้า n = 5

1
12
123
1234
12345
แต่มีเงื่อนไขเพิ่ม:

ถ้าตัวเลขตำแหน่งใดเป็นเลขคู่ → พิมพ์ - แทน

ดังนั้น n = 5 จะได้

1
1-
1-3
1-3-
1-3-5
"""
n = int(input("enter an integer: "))
for i in range(1, n + 1):
    for j in range(1, i+1):
        if j % 2 == 0:
            print("-", end="")
        else:
            print(j, end="")
    print()