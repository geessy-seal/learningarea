"""

ข้อ 3.3

เขียนโปรแกรมรับจำนวนเต็มบวก 1 จำนวนชื่อ limit

ใช้ while loop หาผลรวมของเลขตั้งแต่ 1 ถึง limit

แต่ให้นำมาบวกเฉพาะเลขที่:

เป็นเลขคี่ และไม่หาร 5 ลงตัว

พิมพ์ผลรวมสุดท้ายออกมา
"""
#5
n = int(input("in put: "))
num = 0
c = 1
while c <= n:
    #ไม่เอาหาร 5 ลงคัว
    if c%5 == 0:
        c+=1
    #ไม่เอาหาร 2 ลงตัว
    elif c % 2 == 0:
        c+=1
    else:
        num += c
        c+=1


print(num)


